    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        if (evt.getClickCount() == 2) {
            
            int r = jTable1.getSelectedRow();
            
            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a Product.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                
                String id = jTable1.getValueAt(r, 0).toString();
                String name = jTable1.getValueAt(r, 1).toString();
                String cateory = jTable1.getValueAt(r, 4).toString();
                String sub_category = jTable1.getValueAt(r, 5).toString();
                String brand = jTable1.getValueAt(r, 6).toString();
                
                try {
                    ResultSet rs = MySQL.search("SELECT * FROM `products` INNER JOIN `qty_types` ON `products`.`qty_types_id`=`qty_types`.`id` WHERE `products`.`id`='" + id + "'");
                    rs.next();
                    
                    String qty_type = rs.getString("qty_types.name");
                    
                    grn.jLabel14.setText(id);
                    grn.jLabel15.setText(name);
                    grn.jLabel16.setText(brand);
                    grn.jLabel18.setText(cateory);
                    grn.jLabel20.setText(sub_category);
                    grn.jLabel35.setText(qty_type);
                    
                    this.dispose();
                } catch (Exception e) {
                    e.printStackTrace();
                }
                
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MouseClicked

