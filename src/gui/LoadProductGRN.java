    public static void loadProducts() {
        
        String sid = GRN.jLabel22.getText();
        
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `products` INNER JOIN `brand` ON `products`.`brand_id` = `brand`.`id` INNER JOIN `category` ON `category`.`id` = `products`.`category_id` INNER JOIN `sub_categories` ON `sub_categories`.`id` = `products`.`sub_category_id` INNER JOIN `supplier` ON `supplier`.`id` = `products`.`supplier_id` INNER JOIN `status` ON `status`.`id` = `products`.`status_id` WHERE `supplier`.`id`='" + sid + "' ORDER BY `products`.id ASC;");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            
            while (rs.next()) {
                
                Vector v = new Vector();
                v.add(rs.getString("products.id"));
                v.add(rs.getString("products.name"));
                v.add(rs.getString("products.code"));
                v.add(rs.getString("status.name"));
                v.add(rs.getString("category.name"));
                v.add(rs.getString("sub_categories.name"));
                v.add(rs.getString("brand.name"));
                
                dtm.addRow(v);
                
            }
            
            jTable1.setModel(dtm);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }

