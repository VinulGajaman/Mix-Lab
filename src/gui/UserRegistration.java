    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {

            int r = jTable1.getSelectedRow();
            UserUpdate uu = new UserUpdate(null, true);

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a User.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                String uid = jTable1.getValueAt(r, 0).toString();
                String uname = jTable1.getValueAt(r, 1).toString();
                String username = jTable1.getValueAt(r, 2).toString();
                String password = jTable1.getValueAt(r, 3).toString();
                String number = jTable1.getValueAt(r, 4).toString();
                String role = jTable1.getValueAt(r, 5).toString();
                String status = jTable1.getValueAt(r, 6).toString();

          

                uu.jLabel8.setText(uid);
                uu.jTextField1.setText(uname);
                uu.jTextField2.setText(number);
                uu.jTextField3.setText(username);
                uu.jPasswordField1.setText(password);

                uu.jComboBox1.setSelectedItem(role);
                uu.jComboBox2.setSelectedItem(status);
                uu.setVisible(true);

            }
        }        // TODO add your handling code here:

    }//GEN-LAST:event_jTable1MouseClicked

