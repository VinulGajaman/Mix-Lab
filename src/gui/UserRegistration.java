    public static void loadUsers() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `user` INNER JOIN `role` ON `user`.`role_id` = `role`.`id` INNER JOIN `status` ON `status`.`id`=`user`.`status_id` ORDER BY `user`.id ASC;");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("user.id"));
                v.add(rs.getString("user.name"));
                v.add(rs.getString("user.username"));
                v.add(rs.getString("user.password"));
                v.add(rs.getString("user.mobile"));
                v.add(rs.getString("role.role"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);

            }

            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadUsers(String text) {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `user` INNER JOIN `role` ON `user`.`role_id` = `role`.`id` INNER JOIN `status` ON `status`.`id`=`user`.`status_id` WHERE `user`.`name` LIKE '" + text + "%'  OR `user`.`mobile` LIKE '" + text + "%' ORDER BY `user`.id ASC;");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("user.id"));
                v.add(rs.getString("user.name"));
                v.add(rs.getString("user.username"));
                v.add(rs.getString("user.password"));
                v.add(rs.getString("user.mobile"));
                v.add(rs.getString("role.role"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);

            }

            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

