    public void loadstatus() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `status`");

            Vector v = new Vector();

            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox4.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

